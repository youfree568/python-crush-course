age = 32

if age < 2:
	print("It's a baby!")
elif age >= 2 and age <= 4:
	print("This is a baby!")
elif age > 4 and age < 13:
	print("It's a child!")
elif age >= 13 and age < 20:
	print("This is a teenager!")
elif age >= 20 and age < 65:
	print("It's an adult!")
elif age >= 65:
	print("He is an old man!")